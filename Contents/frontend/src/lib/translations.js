/**
 * Get translation function for a specific language
 * @param {string} langCode - Language code (en, hi, kn, ta, te, bn, mr)
 * @returns {function} - Translation function
 */
export const getTranslation = (langCode = 'en') => {
  const lang = translations[langCode] || translations['en'];
  
  return (key, fallback = key) => {
    return lang[key] || fallback;
  };
};

/**
 * React hook for translations
 * Usage: const t = useTranslations(languageCode);
 */
export const useTranslations = (langCode) => {
  return getTranslation(langCode);
};
